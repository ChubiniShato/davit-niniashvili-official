export const getPublishedArchiveMedia = () => {
  return mediaRecords
    .filter(record => record.recordStatus === "confirmed")
    .sort((a, b) => {
      if (!a.publishedAt && !b.publishedAt) return 0;
      if (!a.publishedAt) return 1;
      if (!b.publishedAt) return -1;
      return b.publishedAt.localeCompare(a.publishedAt);
    });
};

export const getPendingVerificationMedia = () => {
  return mediaRecords
    .filter(record => record.recordStatus === "provisional")
    .sort((a, b) => {
      // Rule 1: ISO dates sort first, descending
      if (a.publishedAt && b.publishedAt) {
        return b.publishedAt.localeCompare(a.publishedAt);
      }
      if (a.publishedAt) return -1;
      if (b.publishedAt) return 1;

      // Rule 2 & 3: Handle displayDate values
      const isYearA = /^\d{4}$/.test(a.displayDate);
      const isYearB = /^\d{4}$/.test(b.displayDate);

      // Both are years: descending
      if (isYearA && isYearB) {
        return b.displayDate.localeCompare(a.displayDate);
      }
      
      // Years before non-date labels
      if (isYearA) return -1;
      if (isYearB) return 1;

      // Non-date labels last (e.g. "Reference")
      return a.displayDate.localeCompare(b.displayDate);
    });
};

